package ru.webfester.parser;

public class SelectorElements {

    String nameText;
    String priceText;
    String priceSizeText;
    String priceDataPriceText;
    String currencyIdText;
    String pictureText;
    String deliveryText;
    String categoryIdText;
    String vendorText;
    String descriptionText;
    String paramText;
    String paramKeyText;
    String paramValueText;
    String urlTestText;
    String textSelectFileURL;

    public SelectorElements(String nameText, String priceText, String priceSizeText,
            String priceDataPriceText, String currencyIdText, String pictureText,
            String deliveryText, String categoryIdText, String vendorText, String descriptionText,
            String paramText, String paramKeyText, String paramValueText, String urlTestText, String textSelectFileURL) {
        this.nameText = nameText;
        this.priceText = priceText;
        this.priceSizeText = priceSizeText;
        this.priceDataPriceText = priceDataPriceText;
        this.currencyIdText = currencyIdText;
        this.pictureText = pictureText;
        this.deliveryText = deliveryText;
        this.categoryIdText = categoryIdText;
        this.vendorText = vendorText;
        this.descriptionText = descriptionText;
        this.paramText = paramText;
        this.paramKeyText = paramKeyText;
        this.paramValueText = paramValueText;
        this.urlTestText = urlTestText;
        this.textSelectFileURL = textSelectFileURL;

    }
}
